#include <bits/stdc++.h>
using namespace std;

int main()
{
    cout << "Printing numbers from 1-100 using while loop: ";
    int i = 1;
    while(i <= 100)
    {
        cout << i << " ";
        i++;
    }
    cout << "\nPrinting numbers from 1-100 using for loop: ";
    for(i = 1; i <= 100; i++)
    {
        cout << i << " ";
    }
    return 0;
}